_OG_IMAGE_TARGET = Path("images") / "og-image.png"


def ensure_social_preview_image(static_root: str | Path, *, logger=None) -> Path:
    """Ensure the OG social preview PNG exists under ``static/images``.

    The binary is embedded as a base64 payload so we can avoid storing new binary
    files in the repository while keeping deployment environments happy.
    """

    root_path = Path(static_root)
    images_dir = root_path / _OG_IMAGE_TARGET.parent
    images_dir.mkdir(parents=True, exist_ok=True)
    target = images_dir / _OG_IMAGE_TARGET.name
    payload = base64.b64decode(OG_IMAGE_BASE64)

    try:
        if target.exists() and target.read_bytes() == payload:
            return target
    except OSError as exc:
        if logger:
            logger.warning("[SEO] Unable to read existing og-image.png: %s", exc)
        else:
            raise

    tmp_path = target.with_suffix(".tmp")
    try:
        tmp_path.write_bytes(payload)
        tmp_path.replace(target)
    except OSError as exc:
        if logger:
            logger.error("[SEO] Unable to refresh og-image.png: %s", exc)
        else:
            raise
    finally:
        if tmp_path.exists():
            try:
                tmp_path.unlink()
            except OSError:
                pass

    if logger:
        logger.info("[SEO] social preview image ensured at %s", target)

    return target
